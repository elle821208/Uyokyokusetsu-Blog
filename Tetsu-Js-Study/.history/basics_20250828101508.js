// ===================================
// ポイントまとめ
// ===================================
// 1. グローバル変数はプログラム全体で使える
// 2. ローカル変数は関数内だけで使える
// 3. 同じ名前の変数をグローバルとローカルで使っても独立している
// 4. 関数は作っただけでは動かない。呼び出して初めて処理が実行される










