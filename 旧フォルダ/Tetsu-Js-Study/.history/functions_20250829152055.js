// (1) entryCode に 200 が渡される
// (2) Math.random() の結果によって当選/外れが決まる
// 　　- 30% の確率 → 戻り値は 200
// 　　- 70% の確率 → 戻り値は 0
// (3) その戻り値を console.log で表示する


// --------------------------------------------------
