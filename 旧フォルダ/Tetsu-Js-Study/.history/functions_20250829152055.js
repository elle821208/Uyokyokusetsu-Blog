// 1. 月を渡すと季節を返す関数
function getSeason(month) {
    // 作業の流れ:
    //  (1) 下記 3. の const season の右辺で new Date() により日時オブジェクトを作成
    //  (2) .getMonth() で「月（0～11）」だけを取り出す
    //  (3) +1 して「1～12」の形式に直す
    //  (4) その値（今回の設定の2025年8月28日より 8）が getSeason の括弧の中の month に入る
    //  (5) 関数内の if 文で判定され、戻り値が返る

    if (month >= 3 && month <= 5) {
        return "春";
    } else if (month >= 6 && month <= 8) {
        return "夏";
    } else if (month >= 9 && month <= 11) {
        return "秋";
    } else {
        return "冬";
    }
}

// 2. 現在の月を取得（currentMonth は const 定数）
const currentMonth = new Date().getMonth() + 1; 
// 作業の流れ:
//  (1) 下記 3. の const season の右辺で new Date() により日時オブジェクトを作成
//  (2) .getMonth() で「月（0～11）」を取り出す
//  (3) +1 して「1～12」の形式に変換
//  (4) その値（今回の設定の2025年8月28日より 8）が currentMonth に格納される

// 3. 関数を呼び出して季節を取得（season は const 定数）
