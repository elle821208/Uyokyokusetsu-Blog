// 1. 引数と戻り値を使った関数定義
function getDarkModeStatus(time) {
    // 作業の流れ:
    //  (1) 下記 const mode の右辺で new Date() により日時オブジェクトを作成
    //  (2) .getHours() で「時間（0～23）」だけを取り出す
    //  (3) その値（今回の設定の2025年8月28日より 21）が getDarkModeStatus の括弧の中の time に入る
    //  (4) 関数内の if 文で処理され、戻り値 ("dark" または "light") が const mode に代入される

    if (time > 20) {
        return "dark";
    } else {
        return "light";
    }
}

// 2. 関数を呼び出して結果を変数に格納（mode は const 定数）
const mode = getDarkModeStatus(new Date().getHours()); 
// 作業の流れ:
//  (1) new Date() で現在の日時（オブジェクト）を取得
//  (2) .getHours() で「時間（0～23）」を厳選
//  (3) その値（今回の設定の2025年8月28日より 21）が getDarkModeStatus の括弧の中の time に入る
//  (4) 関数内で if 文によって処理され、戻り値が mode に代入される

// 3. 背景色を変更する処理
if (mode === "dark") {
    document.body.style.backgroundColor = "#000";
} else {
    document.body.style.backgroundColor = "#fff";
}

// 4. 結果をコンソールに表示
console.log(`2️⃣ 現在のモード: ${mode} （今回の設定の2025年8月28日より dark）`);




