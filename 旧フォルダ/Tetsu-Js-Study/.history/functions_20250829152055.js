//  (1) const season の右辺で getSeason(currentMonth) を呼び出す

//   上記2.の const currentMonthの値が入る

//      → currentMonth の値（今回の設定の2025年8月28日より 8）が getSeason の括弧の中の month に入る
//  (2) 関数内で if 文によって季節が判定される
//  (3) 判定結果（今回の設定の2025年8月28日より 夏）が戻り値となり season に代入される

